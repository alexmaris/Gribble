using System;

namespace Gribble.Model
{
    public class Column
    {
        public Column(
            string name, 
            Type type, 
            int length = 0, 
            bool isNullable = false, 
            bool isIdentity = false, 
            bool isAutoGenerated = false, 
            bool isPrimaryKey = false, 
            object defaultValue = null)
        {
            Name = name;
            Type = type;
            Length = length;
            IsIdentity = isIdentity;
            IsPrimaryKey = isPrimaryKey;
            Length = length;
            IsAutoGenerated = isAutoGenerated;
            IsNullable = isNullable;
            DefaultValue = defaultValue;
        }

        public class OptionBuilder
        {
            private readonly Column _column;
            public OptionBuilder(Column column) { _column = column; }
            public Column Create() { return _column; }
            public OptionBuilder Length(int length) { _column.Length = length; return this; }
            public OptionBuilder IsNullable() { _column.IsNullable = true; return this; }
            public OptionBuilder IsIdentity() { _column.IsIdentity = true; return this; }
            public OptionBuilder IsAutoGenerated() { _column.IsAutoGenerated = true; return this; }
            public OptionBuilder IsPrimaryKey() { _column.IsPrimaryKey = true; return this; }
            public static implicit operator Column(OptionBuilder builder) { return builder._column; }
        }

        public static OptionBuilder Create<T>(string name, T defaultValue)
        {
            return new OptionBuilder(new Column(name, typeof (T), defaultValue: defaultValue));
        }

        public static OptionBuilder Create<T>(string name)
        {
            return new OptionBuilder(new Column(name, typeof(T)));
        }

        public string Name { get; private set; }
        public Type Type { get; private set; }
        public int Length { get; private set; }
        public bool IsNullable { get; private set; }
        public bool IsIdentity { get; private set; }
        public bool IsAutoGenerated { get; private set; }
        public object DefaultValue { get; private set; }
        public bool IsPrimaryKey { get; private set; }
    }
}
